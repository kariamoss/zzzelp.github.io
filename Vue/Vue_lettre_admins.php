		document.querySelector('#resultats_pseudos_' + serveur).style.zIndex = -1;
	}
	else {
		document.querySelector('#resultats_pseudos_' + serveur).style.zIndex = 999;
	}
}

function Validation_donnees(resultat, serveur) {
	document.querySelector('#resultats_pseudos_' + serveur).innerHTML = '';
	document.querySelector('#pseudo_' + serveur).value = resultat;
	document.querySelector('#pseudo_' + serveur).focus();
	document.querySelector('#resultats_pseudos_' + serveur).style.zIndex = -1;
}

function Signer_lettre() {
	var pseudos = document.querySelectorAll('input[id*="pseudo_"]'),
		message = document.querySelector('#message_perso').value,
		adresse = (message == '') ? '' : ('message=' + message);
	for(var i=0; i<pseudos.length; i++) {
		adresse += ((adresse == '') ? '' : '&') + pseudos[i].dataset.serveur + '=' + pseudos[i].value; 
	}
	var xhr = getXMLHttpRequest();
	xhr.onreadystatechange = function() {
		if (xhr.readyState == 4 && (xhr.status == 200 || xhr.status == 0)) {
			var donnees_brutes = xhr.responseText,
				zones = document.querySelectorAll('.zone_rechargeable');
			for(var i=0; i<zones.length; i++) {
				zones[i].innerHTML = donnees_brutes;
			}
			document.querySelector('.detail_vote').style.display = 'none';
			document.querySelector('.messages_persos').style.display = 'none';			
			$('#signer_lettre').popUpWindow({
				action: "close",
			});
			alert('Merci pour votre aide');			
		}
	};
	xhr.open('POST', url_site + '/Ajax/Lettre?mode=modif&zone=1', true);
	xhr.setRequestHeader("Content-Type", "application/x-www-form-urlencoded");
	xhr.send(adresse);
}

