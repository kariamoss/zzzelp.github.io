function Afficher_liste(valeurs, serveur) {
	document.querySelector('#resultats_pseudos_' + serveur).style.width = document.querySelector('#pop-up').offsetWidth + 'px';
	document.querySelector('#resultats_pseudos_' + serveur).innerHTML = '';
	for(var i=0;i<valeurs.length;i++) {
		document.querySelector('#resultats_pseudos_' + serveur).innerHTML += '<div class="ligne_autocomplete" onclick="Validation_donnees(this.innerHTML, \'' + serveur + '\')\">' + valeurs[i] + '</div>';
	}
