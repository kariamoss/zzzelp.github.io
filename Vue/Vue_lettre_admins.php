								<p style="text-indent:25px">
									En décembre, Fourmizzz va souffler sa neuvième bougie, ce n'est donc plus un jeune site loin s'en faut. 
									Comme tout projet, il a connu des périodes plus fastes que d'autres et vous savez comme nous que cette dernière année ne peut pas être qualifiée de bonne année. 
									Les raisons sont sûrement multiples, certaines ne dépendant pas de nous. Néanmoins nous pensons que Fourmizzz a encore du potentiel et nous souhaiterions vous le montrer.
								</p>
								<p style="text-indent:25px">
									Certains d'entres nous ont connu le Fourmizzz des origines, ont vu Amrac, d'abord seul puis épaulé par Preem, créer ce jeu, l'améliorer, 
									demander de l'aide à ses joueurs pour dénicher les bugs et faire de Fourmizzz le jeu qu'il est aujourd'hui.
									Pourtant les temps ont bien changé et plus le temps passe moins nous voyons d'implication de votre part sur Fourmizzz.
									Cette situation ne date pas d'hier mais même en 2013 le développement avait <a target="_BLANK" href="http://fourmizzz.cforum.info/t12996-Les-Nouveaut-s-depuis-le-S4.htm"> été plus visible</a>.
									Deux points principaux ont retenu notre attention, le but premier de cette lettre est de vous les énoncer clairement et d'expliquer en quoi ce manque de réactions de votre
									part est néfaste pour le jeu et a des effets dévastateurs sur la communauté.
									Nous n'aborderons pas ici le cas du <a target="_BLANK" href="http://fourmizzz.cforum.info/t5288-Premiers-Pas-de-Antzzz.htm">serveur anglais</a> malgré son état critique et nous nous concentrerons
									sur les serveurs francophones.
									Nous vous savons capable d'améliorer le jeu, nous voyons bien les évolutions sur <a target="_BLANK" href="http://forum.war2100.fr/viewforum.php?f=12&sid=43261eb3f1c472fb785267d6b38c2090">war2100</a> et il n'y a pas de raisons que vous
									ne puissiez pas réussir à faire de même sur Fourmizzz si vous le voulez.
								</p>
								<p style="text-indent:25px">
									Premièrement, Fourmizzz souffre aujourd'hui d'un manque flagrant de communication de la part des administrateurs, que ce soit au sujet des modifications apportées au jeu ou 
									de la vision sur le long terme. En 2012 vous nous annonciez <a target="_BLANK" href="http://fourmizzz.cforum.info/t10945-S4-apr-s-la-fin-du-monde.htm">réfléchir sur un s4 différent et préparer
									un système de Battle automatique</a>. Vous expliquiez également que votre nouveau statut vous permettrait de passer plus de temps à développer Fourmizzz.
									Pourtant plus de deux ans plus tard, le s4 est un s3-bis et votre temps de développement ne semble pas augmenter loin de là. Nous ne savons presque rien de vos autres projets et sommes donc bien incapables de vous aider
									à les promouvoir. Un suivi plus attentif de la section <i>Amélioration</i> du forum permettrait également de resserer les liens avec la part de votre communauté prête à passer du temps
									pour améliorer votre jeu. Actuellement on voit des propositions argumentées, réfléchies et débatues, plonger dans l'oubli par manque de réaction de votre part.
								</p>
								<p style="text-indent:25px">
									Le second reproche que nous avons à vous faire est le manque de nouvelles fonctionnalités apportées au jeu. Sur l'année passée il n'y a eu qu'une nouveauté majeure, à savoir la 
									messagerie. Les autres ajouts sont d'ordre cosmétiques et mineurs, créant donc une véritable sensation de lassitude face à ce jeu que nous connaissons maintenant trop bien.
									Le système de serveur actuel a atteint ses limites et le s4 en est un exemple flagrant. L'ambiance se détériore à cause d'un gameplay qui n'est plus adapté et encourage les 
									joueurs peu scrupuleux à piller sans relâche les faibles. Commencer sur Fourmizzz est aujourd'hui une véritable galère sans un appui d'une alliance influente (appuie rarement accordé 
									car le jeu n'encourage absolument pas le mélange entre anciens joueurs et débutants).
								</p>
								<p style="text-indent:25px">
									Comment redonner à Fourmizzz son éclat ? Voici quelques idées que nous avons rassemblé pour un nouveau serveur :
									<ul>
										<li>Empêcher le pillage des petits joueurs en limitant l'intérêt de l'XP sur les faibles cibles (pondération en fonction du niveau)</li>
										<li>Revoir le système de distance et/ou de carte qui empèche actuellement le mélange entre les joueurs aguerris et les débutants (une alliance
										active peut difficilement se permettre d'avoir un joueur à 12h de trajet d'elle)</li>
										<li>Ajouter des objectifs au jeu que ce soit pour les joueurs ou pour les alliances</li>
										<li>Encourager et encadrer les développements externes en proposant une interface de gestion intégrée à Fourmizzz</li>
									</ul>
									Plusieurs joueurs ont également abordé l'idée de doter Fourmizzz d'une équipe de bénévoles pouvant vous aider à améliorer et passer du temps sur des points 
									que vous pouvez sous-estimer, n'ayant pas joué au jeu depuis des années.
								</p>
								<p style="text-indent:25px">
									Il est facile de critiquer et beaucoup moins de proposer des solutions. Cette lettre a donc pour objectif de vous montrer que des idées, nous en avons et que nous sommes 
									prêts à en chercher d'autres si nous nous savons écoutés. Acceptez de renouer le dialogue avec nous, de prendre le temps de développer les idées fondamentales que nous 
									vous avons proposé, d'investir plus de temps sur ce jeu. Tout le monde peut sous-estimer l'impact de certaines actions et avoir peur de chercher à les corriger. 
									Notre rôle n'est pas de critiquer mais de vous aider, et tant mieux si cette lettre peut vous en faire prendre conscience. 
									Nous sommes convaincus qu'avec un investissement supérieur de votre part sur le jeu, nous avons tous à y gagner.
									Fourmizzz est un jeu avec un concept originial, là où la plupart ont choisi un thème commun, profitez-en.
								</p>
